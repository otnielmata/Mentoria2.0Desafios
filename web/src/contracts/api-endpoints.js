function normalizeMethod(method) {
  return String(method || "").trim().toUpperCase();
}

function toRouteKey(endpoint) {
  return `${normalizeMethod(endpoint.method)} ${endpoint.path}`;
}

function toApiPath(endpoint) {
  return `${API_BASE_PATH}${endpoint.path}`;
}

function getActiveWebApiEndpoints() {
  return WEB_API_ENDPOINTS.filter((endpoint) => !endpoint.future);
}

function getFutureWebApiEndpoints() {
  return WEB_API_ENDPOINTS.filter((endpoint) => endpoint.future);
}

